def print_help() -> None:
    """
    Print the help for the command to execute the main function correctly.
    """
    print("Right way to execute: ")
    print("    main.py [URL] [OUTPUT_CSV_NAME]")
    print("\nIf you need help try:")
    print("    main.py -h")
    print("\nArguments :")
    print("    URL : an url of a site.")
    print("    OUTPUT_CSV_NAME : the name of the output file in the data folder.")